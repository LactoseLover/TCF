public abstract class abstractObject
{

  protected int _x,_y,_m;

  protected int getX()
  {
      return _x;
  }

  protected int getY()
  {
    return _y;
  }
  protected int getM()
  {
    return _m;
  }

  protected void setX(int v)
  {
    this._x = v;
  }
  protected void setY(int v)
  {
    this._y = v;
  }
  protected void setM(int v)
  {
    this._m = v;
  }

}
